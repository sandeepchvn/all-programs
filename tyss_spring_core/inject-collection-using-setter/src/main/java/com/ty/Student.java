package com.ty;

public class Student {
	private String name;
	private int age;
	private java.util.List<String> subjectName;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public java.util.List<String> getSubjectName() {
		return subjectName;
	}

	public void setSubjectName(java.util.List<String> subjectName) {
		this.subjectName = subjectName;
	}

}
